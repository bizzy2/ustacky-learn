# The backslashes tell python to ignore special meaning ' normally given to string and print
a_message = 'The \'red\' color is the best color'
print (a_message)
print('*'*120)

# Special characters
a_message = 'The red\tcolor is the best color'
print (a_message)
print('*'*120)

a_message = 'The red\n color is the best color'
print (a_message)
print('*'*120)

a_message = 'The red\r color is the best color'
print (a_message)
print('*'*120)

# \b is a backspace
a_message = 'The red\b color is the best color'
print (a_message)
print('*'*120)
